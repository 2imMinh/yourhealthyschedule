const DICTS: Record<Lang, Dict> = { vi, en };

interface I18nValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  list: (key: string) => string[];
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("vi"); // mặc định tiếng Việt

  // Đọc lựa chọn đã lưu sau khi mount (tránh lệch hydration).
  useEffect(() => {
    const saved = typeof window !== "undefined" ? (localStorage.getItem("lang") as Lang | null) : null;
    if (saved === "vi" || saved === "en") setLangState(saved);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    try {
      localStorage.setItem("lang", l);
    } catch {
      /* bỏ qua nếu không lưu được */
    }
  };

  const t = (key: string, vars?: Record<string, string | number>) => {
    const v = DICTS[lang][key];
    let s = typeof v === "string" ? v : key;
    if (vars) for (const [k, val] of Object.entries(vars)) s = s.replace(`{${k}}`, String(val));
    return s;
  };

  const list = (key: string) => {
    const v = DICTS[lang][key];
    return Array.isArray(v) ? v : [];
  };

  return <I18nContext.Provider value={{ lang, setLang, t, list }}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
